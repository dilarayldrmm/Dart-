// Merhaba, ben “John Doe” ve Merhaba, ben “John Doe” ifadelerini tek ve çift tırnak işaretiyle yazdıran bir program yazın .
void main() {
  // 1️⃣ Tek tırnak içinde çift tırnak kullanmak
  print('Merhaba, ben "John Doe"');

  // 2️⃣ Çift tırnak içinde tek tırnak kullanmak
  print("Merhaba, ben 'John Doe'");

  // 3️⃣ Eğer aynı tür tırnak içinde kullanmak istersen, kaçış karakteri (\) kullanılır:
  print("Merhaba, ben \"John Doe\"");
  print('Merhaba, ben \'John Doe\'');
}